public class RequestPayload {
    private String body;
    private String key;
    public String getBody() {
        return body;
    }
    public void setBody(String value) {
        this.body = value;
    }
    public String getKey() {
        return key;
    }
    public void setKey(String value) {
        this.key = value;
    }
}
